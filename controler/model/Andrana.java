package model;

public class Andrana extends Inserable{
    int id;
    String anarana;
    double chiffre;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getAnarana() {
        return anarana;
    }

    public void setAnarana(String anarana) {
        this.anarana = anarana;
    }

    public double getChiffre() {
        return chiffre;
    }
    public void setChiffre(double chiffre) {
        this.chiffre = chiffre;
    }

    
}
